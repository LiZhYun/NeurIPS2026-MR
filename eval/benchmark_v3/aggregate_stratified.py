"""Aggregate v3 eval JSONs by support-strata (present vs absent).

For each method's per-query results + support_strata.json:
  - Compute overall AUC + per-stratum AUC (present, absent)
  - Compute test_test only AUC + per-stratum
  - Bootstrap CI

Output: review-stage/STRATIFIED_RESULTS_2026-04-23.md

Usage: python -m eval.benchmark_v3.aggregate_stratified
"""
from __future__ import annotations
import argparse
import json
import sys
from collections import defaultdict
from pathlib import Path

import numpy as np

PROJECT_ROOT = Path(__file__).resolve().parents[2]

# All known v3 eval dirs
METHOD_PATHS = {
    'anytop_train_v3': 'eval/results/baselines/anytop_train_v3/v3_fold_{F}',
    'k_frame_nn': 'eval/results/baselines/k_frame_nn/v3_fold_{F}',
    'k_retrieve': 'eval/results/baselines/k_retrieve/v3_fold_{F}',
    'm2m_lite': 'eval/results/baselines/m2m_lite/v3_fold_{F}',
    'm2m_official': 'eval/results/baselines/m2m_official/v3_fold_{F}',
    'moreflow_primary_70': 'save/moreflow/v3/primary_70/fold_{F}',
    'moreflow_inductive_60': 'save/moreflow/v3/inductive_60/fold_{F}',
    'ace_primary_70': 'save/ace_inference/v3/ace_primary_70/fold_{F}',
    'ace_inductive_60': 'save/ace_inference/v3/ace_inductive_60/fold_{F}',
    'oracle_action_oracle': 'save/oracles/v3/action_oracle/fold_{F}',
    'oracle_self_positive': 'save/oracles/v3/self_positive/fold_{F}',
    'oracle_random_skel_b': 'save/oracles/v3/random_skel_b/fold_{F}',
    'i5_action_classifier': 'save/oracles/v3/i5_action_classifier/fold_{F}',
}

DISTANCES = ('procrustes', 'zscore_dtw', 'q_component')


def boot_ci(arr, n_boot=500, seed=42, ci=0.95):
    arr = np.asarray(arr, dtype=np.float64)
    if len(arr) == 0:
        return (0.0, 0.0, 0.0)
    rng = np.random.RandomState(seed)
    means = np.array([rng.choice(arr, size=len(arr), replace=True).mean()
                      for _ in range(n_boot)])
    lo = np.percentile(means, (1 - ci) / 2 * 100)
    hi = np.percentile(means, (1 + ci) / 2 * 100)
    return (float(lo), float(arr.mean()), float(hi))


def load_strata(fold):
    p = PROJECT_ROOT / f'eval/benchmark_v3/queries/fold_{fold}/support_strata.json'
    if not p.exists():
        return {}
    s = json.load(open(p))
    return {q['query_id']: q for q in s['queries']}


def aggregate_method(method, dist, fold, strata):
    p = PROJECT_ROOT / METHOD_PATHS[method].format(F=fold) / f'v3_eval_{dist}.json'
    if not p.exists():
        return None
    d = json.load(open(p))
    if 'per_query' not in d:
        return None
    pq = d['per_query']
    # Group AUC by stratum
    by_stratum = {'present': [], 'absent': []}
    by_split_stratum = {'test_test_present': [], 'test_test_absent': [],
                        'all_present': [], 'all_absent': []}
    for r in pq:
        qid = r['query_id']
        sp_meta = strata.get(qid)
        if sp_meta is None:
            continue
        sp = 'present' if sp_meta['support_present'] else 'absent'
        by_stratum[sp].append(r['auc'])
        by_split_stratum[f'all_{sp}'].append(r['auc'])
        if r['split'] == 'test_test':
            by_split_stratum[f'test_test_{sp}'].append(r['auc'])
    return {
        'present_auc_ci': boot_ci(by_stratum['present']),
        'absent_auc_ci': boot_ci(by_stratum['absent']),
        'tt_present_auc_ci': boot_ci(by_split_stratum['test_test_present']),
        'tt_absent_auc_ci': boot_ci(by_split_stratum['test_test_absent']),
        'n_present': len(by_stratum['present']),
        'n_absent': len(by_stratum['absent']),
        'n_tt_present': len(by_split_stratum['test_test_present']),
        'n_tt_absent': len(by_split_stratum['test_test_absent']),
    }


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument('--folds', nargs='+', type=int, default=[42, 43])
    parser.add_argument('--out', default='review-stage/STRATIFIED_RESULTS_2026-04-23.md')
    args = parser.parse_args()

    out_lines = ['# Stratified v3 Results (2026-04-23)\n']
    out_lines.append('Generated by `eval.benchmark_v3.aggregate_stratified`. '
                     'Per-query AUC grouped by (support_present vs support_absent), '
                     'optionally restricted to test_test split (held-out skels).\n')
    out_lines.append('Bootstrap 95% CI shown.\n\n')

    # Build aggregate across folds
    for dist in DISTANCES:
        out_lines.append(f'## Distance: {dist}\n')
        out_lines.append('| method | All Present | All Absent | test_test Present | test_test Absent |')
        out_lines.append('|---|---|---|---|---|')
        for method in METHOD_PATHS:
            # Aggregate across folds
            agg = {'present': [], 'absent': [], 'tt_present': [], 'tt_absent': []}
            for fold in args.folds:
                strata = load_strata(fold)
                p = PROJECT_ROOT / METHOD_PATHS[method].format(F=fold) / f'v3_eval_{dist}.json'
                if not p.exists():
                    continue
                d = json.load(open(p))
                if 'per_query' not in d: continue
                for r in d['per_query']:
                    sp_meta = strata.get(r['query_id'])
                    if sp_meta is None: continue
                    sp = 'present' if sp_meta['support_present'] else 'absent'
                    agg[sp].append(r['auc'])
                    if r['split'] == 'test_test':
                        agg[f'tt_{sp}'].append(r['auc'])
            row = f'| {method} '
            for k in ('present', 'absent', 'tt_present', 'tt_absent'):
                ci = boot_ci(agg[k])
                if len(agg[k]) == 0:
                    row += '| — '
                else:
                    row += f'| {ci[1]:.3f} [{ci[0]:.3f},{ci[2]:.3f}] (n={len(agg[k])}) '
            row += '|'
            out_lines.append(row)
        out_lines.append('')

    out_path = PROJECT_ROOT / args.out
    out_path.parent.mkdir(exist_ok=True)
    with open(out_path, 'w') as f:
        f.write('\n'.join(out_lines))
    print(f"Saved: {out_path}")


if __name__ == '__main__':
    main()
